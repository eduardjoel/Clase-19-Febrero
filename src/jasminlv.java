import java.util.Scanner;


	public class jasminlv {

	public static void main(String[] args) {
		System.out.print("Ingrese un Numero");
		Scanner volado=new Scanner (System.in);
		string animal=volado.nextInt();
		
		switch (animal)
		{
			case 1:
			System.out.println("Es un Uno");
			break;
		
			case 2:
				System.out.print("Es un Dos");
				break;
		
		
			case 3:
				System.out.print("Es un Tres");
				break;
		
		
			default:
				System.out.print("Es otro");
				break;
		}
	}
	}

